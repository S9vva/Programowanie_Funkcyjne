#len()
my_list = [1, 2, 3, 4, 5]
print(len(my_list))  # Wydrukuje: 5

#max()
my_list = [1, 2, 3, 4, 5]
print(max(my_list))  # Wydrukuje: 5

#min()
my_list = [1, 2, 3, 4, 5]
print(min(my_list))  # Wydrukuje: 1

